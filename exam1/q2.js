//for loop
console.log("code for (for) loop");
for(let i=1;i<=100;i++){
    console.log("Hello World")
}

//while loop
console.log("code for (while) loop");
let j=0;
while(j<100){
    console.log("Hello World")
    j++
}
//Do while loop
console.log("code for (Do while) loop");
let k=0
do{
    console.log("Hello World")
    k++
}while(k<100)